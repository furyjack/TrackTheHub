package play.android.com.trackthehub.util;



public class Issues {


    public String Reponame;


    public String desc;


    public String number;


    public String date;


    public String user;


    public Issues(String reponame, String desc, String number, String date, String user) {
        Reponame = reponame;
        this.desc = desc;
        this.number = number;
        this.date = date;
        this.user = user;
    }





}
